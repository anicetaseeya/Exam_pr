# A string is something like a printed map, you cannot remove countries from there , while a list is like a map drawn by pencil (you can remove countries and create your new world order).

printed_map = "Ukraine"
printed_map = "slay" + printed_map[1:]
print(printed_map)

pencil_map = ["France", "Germany", "Italy"]
pencil_map[1] = "Spain"
print(pencil_map)